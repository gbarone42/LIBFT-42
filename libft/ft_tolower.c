#include <stdio.h>

int ft_toupper(int c)
{
    if ((c >= 'A' && c <= 'Z'))
        return (c + 32);
    else
        return (c);
}

int main(void)
{
    char c = 'A';
    printf("%c", ft_toupper(c));
    return (0);
}
